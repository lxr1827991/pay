	String openid;
	//商户订单号
	String outTradeNo;
	//支付标题
	String title;
	//支付内容
	String content;
	//总金额
	BigDecimal totalFee;
	//支付通知url
	String notifyUrl;
	//jsapi支付
	String userIp;
	//native支付
	String serverIp;
	//native支付
	String productId;

	public String getOpenid() {
		return openid;
	}

	public void setOpenid(String openid) {
		this.openid = openid;
	}

	public String getOutTradeNo() {
		return outTradeNo;
	}

	public void setOutTradeNo(String outTradeNo) {
		this.outTradeNo = outTradeNo;
	}

	public String getTitle() {
		return title;
	}

